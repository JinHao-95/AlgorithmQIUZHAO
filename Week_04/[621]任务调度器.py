class Solution:
    def leastInterval(self, tasks: List[str], n: int) -> int:
        """
         :type tasks: List[str]
         :type n: int
         :rtype: int
         """
        length = len(tasks)
        if length <= 1:
            return length

        # ç”¨äºŽè®°å½•æ¯ä¸ªä»»åŠ¡å‡ºçŽ°çš„æ¬¡æ•°
        task_map = dict()
        for task in tasks:
            task_map[task] = task_map.get(task, 0) + 1
        # æŒ‰ä»»åŠ¡å‡ºçŽ°çš„æ¬¡æ•°ä»Žå¤§åˆ°å°æŽ’åº
        task_sort = sorted(task_map.items(), key=lambda x: x[1], reverse=True)

        # å‡ºçŽ°æœ€å¤šæ¬¡ä»»åŠ¡çš„æ¬¡æ•°
        max_task_count = task_sort[0][1]
        # è‡³å°‘éœ€è¦çš„æœ€çŸ­æ—¶é—´
        res = (max_task_count - 1) * (n + 1)

        for sort in task_sort:
            if sort[1] == max_task_count:
                res += 1

        # å¦‚æžœç»“æžœæ¯”ä»»åŠ¡æ•°é‡å°‘ï¼Œåˆ™è¿”å›žæ€»ä»»åŠ¡æ•°
        return res if res >= length else length

